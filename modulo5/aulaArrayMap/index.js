//map
const arr = [1, 2, 3];
const newArr = arr.map((num) => num * 2);
console.log(newArr);

//foreach
const arr2 = [1, 2, 3];
arr2.forEach((num) => console.log(num * 2));

// o map nao altera o array original, mas cria um novo

//o foreach nao altera o array original, mas nao cria um novo, ele utiliza dos elementos para manipular
